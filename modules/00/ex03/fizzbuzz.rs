fn main() {
    for n in 1..=100 {
        match (n % 3, n % 5, n % 11) {
            (0, 0, _) => println!("FizzBuzz"),
            (0, _, _) => println!("Fizz"),
            (_, 0, _) => println!("Buzz"),
            (_, _, 3) => println!("FIZZ"),
            (_, _, 5) => println!("BUZZ"),
            (_, _, _) => println!("{}", n),
        }
    }
}